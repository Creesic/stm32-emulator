use std::collections::{BTreeMap, BTreeSet};
use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use anyhow::{bail, Context, Result};
use serde_yaml::Value;

#[derive(Debug)]
struct Variant {
    id: String,
    board_slug: String,
    display_name: String,
    mcu_family: Option<String>,
}

fn main() -> Result<()> {
    let source_root = env::args()
        .nth(1)
        .map(PathBuf::from)
        .context("usage: generate-variant-registry <epicefi_fw root>")?;
    print!("{}", render_registry(&source_root)?);
    Ok(())
}

fn render_registry(source_root: &Path) -> Result<String> {
    let boards_root = source_root.join("firmware").join("config").join("boards");
    let mut variants = Vec::new();
    collect_variants(&boards_root, &boards_root, &mut variants)?;
    disambiguate_ids(&mut variants);
    variants.sort_by(|left, right| left.id.cmp(&right.id));

    let mut seen = BTreeSet::new();
    for variant in &variants {
        if !seen.insert(&variant.id) {
            bail!("duplicate stable variant ID '{}'", variant.id);
        }
    }

    let mut output = String::from(
        "// Generated by generate-variant-registry. Do not edit by hand.\n\n\
         use super::GeneratedVariant;\n\n\
         pub(super) const GENERATED_VARIANTS: &[GeneratedVariant] = &[\n",
    );
    for variant in variants {
        output.push_str("    GeneratedVariant {\n");
        output.push_str(&format!("        id: {:?},\n", variant.id));
        output.push_str(&format!(
            "        display_name: {:?},\n",
            variant.display_name
        ));
        match variant.mcu_family {
            Some(mcu_family) => {
                output.push_str(&format!("        mcu_family: Some({mcu_family:?}),\n"));
            }
            None => output.push_str("        mcu_family: None,\n"),
        }
        output.push_str("    },\n");
    }
    output.push_str("];\n");
    Ok(output)
}

fn collect_variants(boards_root: &Path, path: &Path, variants: &mut Vec<Variant>) -> Result<()> {
    for entry in fs::read_dir(path).with_context(|| format!("reading {}", path.display()))? {
        let entry = entry?;
        let entry_path = entry.path();
        if entry.file_type()?.is_dir() {
            collect_variants(boards_root, &entry_path, variants)?;
        } else if entry.file_name() == "board.yaml" {
            variants.extend(read_board_variants(boards_root, &entry_path)?);
        }
    }
    Ok(())
}

fn read_board_variants(boards_root: &Path, path: &Path) -> Result<Vec<Variant>> {
    let source = fs::read_to_string(path).with_context(|| format!("reading {}", path.display()))?;
    let board: Value =
        serde_yaml::from_str(&source).with_context(|| format!("parsing {}", path.display()))?;
    let description = board
        .get("description")
        .and_then(Value::as_str)
        .or_else(|| board.get("name").and_then(Value::as_str))
        .unwrap_or("EpicEFI board");
    let variants = board
        .get("variants")
        .and_then(Value::as_mapping)
        .map(read_variant_map)
        .transpose()?
        .unwrap_or_default();
    let board_slug = path
        .parent()
        .and_then(|parent| parent.strip_prefix(boards_root).ok())
        .context("board.yaml must be inside the boards root")?
        .components()
        .map(|component| component.as_os_str().to_string_lossy())
        .collect::<Vec<_>>()
        .join("__")
        .to_ascii_lowercase();

    Ok(variants
        .into_iter()
        .map(|(id, settings)| Variant {
            board_slug: board_slug.clone(),
            display_name: format!("{description} / {id}"),
            mcu_family: settings
                .get(&Value::String("PROJECT_CPU".to_owned()))
                .and_then(Value::as_str)
                .map(ToOwned::to_owned),
            id,
        })
        .collect())
}

fn disambiguate_ids(variants: &mut [Variant]) {
    let mut counts = BTreeMap::new();
    for variant in variants.iter() {
        *counts.entry(variant.id.clone()).or_insert(0_usize) += 1;
    }
    for variant in variants.iter_mut() {
        if counts[variant.id.as_str()] > 1 {
            variant.id = format!("{}__{}", variant.board_slug, variant.id);
        }
    }
}

fn read_variant_map(map: &serde_yaml::Mapping) -> Result<BTreeMap<String, serde_yaml::Mapping>> {
    map.iter()
        .map(|(id, settings)| {
            let id = id
                .as_str()
                .context("variant IDs must be strings")?
                .to_owned();
            let settings = settings
                .as_mapping()
                .cloned()
                .context("variant settings must be mappings")?;
            Ok((id, settings))
        })
        .collect()
}

#[cfg(test)]
mod tests {
    use super::{disambiguate_ids, read_variant_map, Variant};
    use serde_yaml::Value;

    #[test]
    fn reads_variant_ids_and_cpu_families() {
        let document: Value =
            serde_yaml::from_str("variants:\n  demo:\n    PROJECT_CPU: ARCH_STM32F7\n").unwrap();
        let variants = read_variant_map(document["variants"].as_mapping().unwrap()).unwrap();

        assert_eq!(
            variants["demo"]
                .get(&Value::String("PROJECT_CPU".to_owned()))
                .and_then(Value::as_str),
            Some("ARCH_STM32F7")
        );
    }

    #[test]
    fn qualifies_only_duplicate_variant_ids_with_the_board_path() {
        let mut variants = vec![
            Variant {
                id: "shared".to_owned(),
                board_slug: "hellen__uaefi".to_owned(),
                display_name: "Hellen uAEFI / shared".to_owned(),
                mcu_family: None,
            },
            Variant {
                id: "shared".to_owned(),
                board_slug: "hellen__uaefi_bigfuel".to_owned(),
                display_name: "Hellen uAEFI BigFuel / shared".to_owned(),
                mcu_family: None,
            },
            Variant {
                id: "proteus_f7".to_owned(),
                board_slug: "proteus".to_owned(),
                display_name: "Proteus / proteus_f7".to_owned(),
                mcu_family: None,
            },
        ];

        disambiguate_ids(&mut variants);

        assert_eq!(variants[0].id, "hellen__uaefi__shared");
        assert_eq!(variants[1].id, "hellen__uaefi_bigfuel__shared");
        assert_eq!(variants[2].id, "proteus_f7");
    }
}
